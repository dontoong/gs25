# 바코드 스캐너
class Barcode_start(QThread):
    signal1 = pyqtSignal(str,str,int,int)

    def __init__(self):
        super().__init__()
        self.cap = cv2.VideoCapture(0)
        self.cnt = 0
        # self.check = ['']
        self.product_name = ''
        self.price = 0
        self.sum_price = 0
        # self.sell_object = []

    def run(self):

        print("실행!")
        while True:
            success, frame = self.cap.read()

            if success:
                for code in pyzbar.decode(frame):
                    if keyboard.is_pressed('p'):
                        # if mouse.is_pressed("left"):
                        my_code = code.data.decode('utf-8')
                        print("현재 바코드 : ", my_code)
                        playsound("qrbarcode_beep.mp3")
                        param = my_code
                        query = f"insert into barcode values ({param})"
                        cursor.execute("delete from barcode")
                        cursor.execute(query)

                        cursor.execute(
                            "select product_name from gs25 where product_barcode = (select * from barcode)")

                        out_data = cursor.fetchall()
                        result = ' '.join(str(s) for s in out_data)
                        self.product_name = result[2:-3]

                        cursor.execute("select price from gs25 where product_barcode = (select * from barcode)")

                        out_data = cursor.fetchall()
                        #print(out_data)
                        result = ' '.join(str(s) for s in out_data)
                        self.price = result[1:-2]

                        # self.timeout.emit(self.product_name)
                        print(self.product_name)
                        print(self.price)

                        self.sum_price = self.sum_price + int(self.price)
                        print(self.sum_price)
                        self.cnt = self.cnt + 1
                        print(self.cnt)

                        self.signal1.emit(self.product_name,self.price,self.cnt,self.sum_price)

        self.cap.release()
        cv2.destroyAllWindows()
        cursor.close()
        con.commit()
        con.close()

